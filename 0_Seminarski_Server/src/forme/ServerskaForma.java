    private void jButtonZaustaviSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZaustaviSActionPerformed
        // TODO add your handling code here:
        if (server != null) {
            server.zaustaviServer();
            jLabelStatus.setText("Server je zaustavljen!");
            jButtonPokreniS.setEnabled(true);
            jButtonZaustaviS.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this, "Server nije pokrenut.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonZaustaviSActionPerformed

