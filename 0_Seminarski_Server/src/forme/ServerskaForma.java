    private void jButtonPokreniSActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPokreniSActionPerformed
        // TODO add your handling code here:
        if (server == null || !server.isAlive()) {
            server = new Server(); // Ključna promena: Nova instanca servera
            server.start();
            jLabelStatus.setText("Server je pokrenut!");
            jButtonPokreniS.setEnabled(false);
            jButtonZaustaviS.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(this, "Server je već pokrenut.", "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPokreniSActionPerformed

